#include <iostream>
using namespace std;

int main()
{
    int a, b;

    cout << "Enter a :" << endl;
    cin >> a;
    cout << "Enter b :" << endl;
    cin >> b;

    cout << "Add = " << a + b;
    cout << "Sub = " << a - b;
    cout << "Mul = " << a * b;
    cout << "Div = " << a / b;

    return 0;
}